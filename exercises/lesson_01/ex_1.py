# Requirement:
# Write a program that uses a for loop to print
#   One of the months of the year is January
#   One of the months of the year is February
#   ...

# Implemented on Python 3.

MONTHS = [
    'January',
    'February',
    'March',
    'April',
    'May',
    'June',
    'July',
    'August',
    'September',
    'October',
    'November',
    'December'
]

for month in MONTHS:
    print('One of the months of the year is ' + month)
